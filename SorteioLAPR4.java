    private void btnAssignActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAssignActionPerformed
        if (listCases.getItemCount() == listMembers.getItemCount()) {
            HashMap<Integer, String> mapMembers = devolverSorteio(listMembers.getItems());
            HashMap<Integer, String> mapCases = devolverSorteio(listCases.getItems());

            DefaultTableModel model = (DefaultTableModel) jTable2.getModel();

            ArrayList<String> list = new ArrayList<>();
            for (int i = 1; i <= mapCases.size(); i++) {
                list.add(mapMembers.get(i));
                list.add(mapCases.get(i));
                model.addRow(list.toArray());
                list.clear();
            }
        }
    }//GEN-LAST:event_btnAssignActionPerformed

