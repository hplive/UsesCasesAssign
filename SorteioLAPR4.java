    private HashMap<Integer, String> devolverSorteio(String[] data) {

        int nr = data.length;
        HashMap<Integer, String> sorteio = new HashMap<>();
        while (sorteio.size() != nr) {
            Random rand = new Random();
            int i = rand.nextInt(nr) + 1;
            sorteio.put(i, "");

        }
//        System.out.println("nr " + nr + "|sorteio.size() " + sorteio.get(1));
        ArrayList<String> list = new ArrayList<>();
        Collections.addAll(list, data);

        while (!list.isEmpty()) {
            Random rand = new Random();
            int i = rand.nextInt(nr) + 1;
            int j = rand.nextInt(list.size());
            if (sorteio.get(i).equals("")) {
                sorteio.put(i, list.get(j));
                list.remove(j);
            }
        }

        return sorteio;

    }

